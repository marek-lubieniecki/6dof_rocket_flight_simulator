function a = speed_sound (T)

kappa = 1.4; #wspolczynnik adiabaty dla powietrza
R = 8.314; #uniwersalna sta³a gazowa
M = 0.028; #masa molowa powietrza

a = sqrt(kappa * R * T / M);

endfunction
